    private DefaultTableModel montarTabela() {
        String[] colunas = {"Data", "Hora", "Pressão sistólica", "Pressão diastólica", "Está sob estresse?"};

        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        ArrayList<dadosMedicao> lista = DadosCSV.ListarMedicao();

        for (int i = 0; i < lista.size(); i++) {
            dadosMedicao d = lista.get(i);

            String[] linhas = {
                d.getData(),
                d.getHora(),
                String.valueOf(d.getPressaoSistolica()),
                String.valueOf(d.getPressaoDiastolica()),
                d.getEstresse()
            };

            tabela.addRow(linhas);
        }
        return tabela;
    }

