package IMC.model;

public class GastoCalorico {

    private double pesoCal;
    private int idadeCal, alturaCal;

    public double getPesoCal() {
        return pesoCal;
    }

    public void setPesoCal(double pesoCal) {
        this.pesoCal = pesoCal;
    }

    public int getIdadeCal() {
        return idadeCal;
    }

    public void setIdadeCal(int idadeCal) {
        this.idadeCal = idadeCal;
    }

    public int getAlturaCal() {
        return alturaCal;
    }

    public void setAlturaCal(int alturaCal) {
        this.alturaCal = alturaCal;
    }

}
